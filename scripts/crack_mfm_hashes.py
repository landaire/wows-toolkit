names = set(raw_names)
extra = set()
for n in list(names):
    # camelCase -> snake_case
    snake = re.sub(r"(?<!^)(?=[A-Z])", "_", n).lower()
    if snake != n.lower():
        extra.add(snake)
    # Map/Texture/Tex suffix variations
    if n.endswith("Map"):
        extra.update([n[:-3] + "Texture", n[:-3] + "Tex", n[:-3]])
    elif n.endswith("Texture"):
        extra.update([n[:-7] + "Map", n[:-7] + "Tex", n[:-7]])

names.update(extra)

print(f"Testing {len(names)} candidates against {len(TARGET)} hashes")
found = {}
for n in sorted(names):
    h = murmurhash3_32(n)
    if h in TARGET:
        found[h] = n
        print(f"  MATCH: {n} -> 0x{h:08x} = {INFO.get(h, '?')}")

print(f"\n=== Summary: {len(found)}/{len(TARGET)} cracked ===")
for h in sorted(TARGET):
    name = found.get(h, "???")
    print(f"  0x{h:08x} {INFO.get(h, '?'):30s} = {name}")

remaining = TARGET - set(found.keys())
if remaining:
    print(f"\n{len(remaining)} hashes still uncracked:")
    for h in sorted(remaining):
        print(f"  0x{h:08x}  {INFO.get(h, '?')}")
